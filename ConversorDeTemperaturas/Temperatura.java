package ConversorDeTemperaturas;

public class Temperatura {
    private float farenheit;
    private float celsius;
    private float kelvin;
    
    public float getFarenheit() {
        return this.farenheit;
    }
    
    public void setFarenheit(float f) {
        this.farenheit = f;
    }
    
    public float getCelsius() {
        return this.celsius;
    }
    
    public void setCelsius(float c) {
        this.celsius = c ;
    }
    
    public float getKelvin() {
        return this.kelvin;
    }
    
    public void setKelvin(float k) {
        this.kelvin = k;
    }
    
    
    
}
